// Define the age of the person
let age: number = 25; // You can change this value to test different outputs

// Check the age and determine the stage of life
if (age < 2) {
    console.log("This person is a baby.");
} else if (age < 4) {
    console.log("This person is a toddler.");
} else if (age < 13) {
    console.log("This person is a kid.");
} else if (age < 20) {
    console.log("This person is a teenager.");
} else if (age < 65) {
    console.log("This person is an adult.");
} else {
    console.log("This person is an elder.");
}